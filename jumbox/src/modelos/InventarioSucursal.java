package modelos;


public class InventarioSucursal {
	private int idInventario;
	
	public InventarioSucursal(int idInventario) {
		super();
		this.idInventario = idInventario;
	}

	public int getIdInventario() {
		return idInventario;
	}

	public void setIdInventario(int idInventario) {
		this.idInventario = idInventario;
	}

}
